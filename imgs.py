def img_request(request, request_count):
    request_count = int(request_count)
    headers = {'User-Agent': get_ua()}
    url = f'https://yandex.ru/images/search?text={request}'
    page = requests.get(url, headers=headers)
    soup = BeautifulSoup(page.text, "html.parser")
    images_list = soup.findAll('img', 'serp-item__thumb justifier__thumb')
    return [f"https:{i['src']}" for i in images_list][:request_count]
